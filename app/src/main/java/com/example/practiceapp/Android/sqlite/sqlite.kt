package com.example.practiceapp.Android.sqlite

/*SQLite is an open-source relational database management system that's built into Android.
 It's used to store, manipulate, and retrieve data from a database.
 SQLite is lightweight, compact, and doesn't require a server to run.
 It's also self-contained, serverless, and requires zero configuration

 Here are some features of SQLite:
 ----------------------------------
Storage: Stores data in a user's device as a text file
Operations: Supports many operations on data, including adding, updating, reading, and deleting
Integration: Easily integrated into mobile applications
Libraries and classes: Many libraries and classes available for database queues

Some disadvantages of using SQLite include:
---------------------------------------------
No compile-time verification of raw SQL queries
Affected queries need to be updated manually if the database structure changes
Requires writing a lot of boilerplate code to connect and transform SQL queries and data objects

 */