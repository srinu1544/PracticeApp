/*Android product flavors are a way to create different versions of an app.
They are also known as Android build types or Android build variants.

Product flavors are useful when you want to create multiple versions of your app using
the same base code. They can have different features, such as:
Being free or paid
Having different themes and texts
Using different environments or APIs
To create product flavors, you need to define rules in the build.gradle.
To run flavors in Android Studio, you can:
Open the Project pane and select the Project view from the drop-down menu at the top of the pane.
Navigate to MyProject/app/src.
Right-click on src and add a java folder.
Under Target source set choose the one for your flavor and click Finish.*/