Time complexity is a measure of the time required to run an algorithm as the input size
increases. It's represented using Big O notation.

Time complexity is highly dependent on the size of the processed data. It's an approximate
estimation of how much time an algorithm will take for large value of input size.

The different types of time complexity notations are:
Constant time – O (1)
Linear time – O (n)
Logarithmic time – O (log n)
Quadratic time – O (n^2)
Cubic time – O (n^3)

Time complexity is independent of the actual execution time of the machine on which the
 algorithm is running on*/