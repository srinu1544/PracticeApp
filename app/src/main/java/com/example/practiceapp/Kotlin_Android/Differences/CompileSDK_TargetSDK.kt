/*
In Android development, the compile SDK version is the minimum version of Android SDK that can be
used to compile an application. The target SDK version is the version of Android that an app was
designed to run on.

The compile SDK version provides the libraries and APIs for the build process.
The compile SDK version can be changed while developing an application.

The target SDK version is the version against which an application has been tested and optimized.
 The target SDK version cannot be higher than the compile SDK version. Ideally, the compile SDK
 version and the target SDK version should be equal and both point to the latest SDK.

Starting on August 31, 2023, new apps must target Android 13 (API level 33) or higher.
Wear OS apps must target a version between Android 11 (API level 30) and Android 13 (API level 33).*/