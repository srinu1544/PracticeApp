package com.example.practiceapp.Kotlin_Android.Testing

/*JUnit
A unit testing framework for Java applications. It's included by default in Android Studio.
JUnit is used to write and execute automated tests. It also supports instrumentation testing.

It's an open-source framework.
It's part of a family of unit testing frameworks known as xUnit.
It includes annotations such as @Test, @Before, and @After.

@BeforeClass, @AfterClass, @Before, @After, @Test, @RepeatedTest.


@Test: This annotation tells JUnit that the public void method to which it is attached can be run as
a test case.

@Before:This annotation is used when different test cases share the same logic. The method with the @Before
annotation always runs before the execution of each test case.

@After: This annotation is used when different test cases share the same logic. The method with
the @After annotation always runs after the execution of each test case.

@BeforeClass: This annotation is used when different test cases share the same logic. The method with
the @BeforeClass annotation always runs before the execution of all the test cases.

@AfterClass: This annotation is used when different test cases share the same logic. The method with
the @AfterClass annotation always runs after the execution of all the test cases.



*/