/*SHA-1 stands for Secure Hash Algorithm 1. It's used in Android app development for:
Signing and verifying digital certificates
Authenticating users
Securing data transfers
Integrating Google API into an app

SHA-1 is required for the following:
------------------------------------
Dynamic Links
Google Sign-in
Phone number support in Auth
Firebase services

To get the SHA-1 key in Android Studio, you can:
------------------------------------------------
Click on the Gradle icon in the top right
Click on the Elephant icon
Type Signing report and press Enter
You can also use the command window or terminal to get the SHA-1 key*/