/*JUnit
-------
A unit testing framework for Java applications. It's included by default in Android Studio.
JUnit is used to write and execute automated tests. It also supports instrumentation testing.

It's an open-source framework.
It's part of a family of unit testing frameworks known as xUnit.
It includes annotations such as @Test, @Before, and @After.

Espresso
--------
A UI testing framework that runs on a real or emulated device. It's used to perform actions and
assertions on UI elements. Espresso extends JUnit classes to support Android-based instrumentation testing.

It provides basic validation that the UI is working as expected.
It's used to perform actions and assertions on UI elements.

