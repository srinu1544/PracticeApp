/*The static keyword in Java is a non-access modifier that is used to declare static members
of a class. Static members are shared among all instances of a class, and they are not tied
to any particular object.

Static members can be variables, methods, or blocks of code. Static variables are initialized
when the class is loaded, and they retain their values throughout the life of the program.
Static methods can be called without creating an instance of the class, and they can access
static variables. Static blocks of code are executed when the class is loaded.*/