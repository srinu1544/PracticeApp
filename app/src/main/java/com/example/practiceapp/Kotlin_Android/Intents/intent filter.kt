package com.example.practiceapp.Kotlin_Android.Intents

/*In Android, an intent filter is an expression in an app's manifest file that specifies
the type of intents that a component can receive. Intent filters declare the capabilities
of their parent components, such as activities, services, or broadcast receivers

An intent filter declares the capabilities of its parent component: what an activity or
service can do and what types of broadcasts a receiver can handle. It opens the component
to receiving intents of the advertised type while filtering out those that aren't meaningful
for the component

*/