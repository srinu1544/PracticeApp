/*


Interceptors are a feature in Retrofit, an HTTP client library for Android.
They allow you to modify outgoing requests and incoming responses before they
are sent and received by the server.

You can use interceptors for many things, including:
Refreshing access tokens
Error handling
Adding other headers
Gzipping requests
Monitoring, rewriting, and retrying API calls
Logging outgoing requests and incoming responses
Adding loggers centrally
Updating AuthToken for failed requests

Interceptors function similarly to airport security personnel during the security check
process.

 */