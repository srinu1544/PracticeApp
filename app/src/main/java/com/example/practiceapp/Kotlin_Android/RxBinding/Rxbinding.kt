package com.example.practiceapp.Kotlin_Android.RxBinding

/*RxBinding is a set of libraries that allows developers to respond to user interface events in
their programs. RxBinding uses the RxJava paradigm to react to events.
RxBinding provides RxJava binding APIs for Android User Interface (UI)
widgets. This allows developers to translate Android UI events into Observable
streams. For example, developers can use RxBinding to execute an asynchronous
network request from a button click event.

RxBinding can also prevent threading problems and memory leaks by using Schedulers
from the RxAndroid library */